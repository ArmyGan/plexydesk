				<h2>Social Networking [Intermediate skills]</h2>
				<p>
					PlexyDesk has an easy to use API to write backed services, which allows you to bring remote desktop services way down to your desktop. There are so many social networks that we use today. Google has a big umbrella of such services, and also a great web API to connect to them. There for one idea is, you can write backed services to connect to Google web services and also associate them with PlexyDesk .
					<br /><br /> Skills required :
				</p>
				<ul class="list">
					<li>Understanding of Google services.</li>
					<li>Understanding of how to write plexy DataEngines.</li>
					<li>Bit of C and manipulating characters and bytes.</li>
					<li>Good understanding of Qt 4 Network API.</li>
					<li>XML is a MUST reading and writing with Qt API or any API which has a compatible license we can accept.</li>
				</ul>
				<h2>Fully working VOIP Data Engine for PlexyDesk [Expert Skills]</h2>
				<p>
					Digital Communication is an integral part of our society, and we have so many clients with cluttered user interfaces which look the same. It’s time you change that. Tapioca is a fantastic framework which handles VOIP, and has support for many services provided by Yahoo and Microsoft and not necessarily Google. your plexy engine should, sync and work in harmony with Plexydesk global contact manager, provide easy access to voip functions.We only expect you to complete the MSN/Yahoo connection managers for this idea set.
					<br /><br /> Skills required :
				</p>
				<ul class="list">
					<li>Understanding of Telepathy framework.</li>
					<li>Understanding of Tapioca and Tapioca-Qt API.</li>
					<li>DBus.</li>
					<li>Good understanding of Qt and it’s data types.</li>
					<li>XML is a MUST reading and writing with Qt API or any API which has a compatible license we can accept.</li>
					<li>Qt GUI experience is just optional.</li>
				</ul>
