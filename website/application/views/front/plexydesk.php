				<h1>PlexyDesk</h1>
				<p>
					We started project <a href="http://www.kbfx.org/"><b>KBFX</b></a> (http://en.wikipedia.org/wiki/KBFX_(KDE)) to bring an alternative start menu for KDE in 2005. 
					Then with time the user base as well as the developer base grew and KBFX was moved to KDE svn.
					At this point we needed to do some thing bigger and better, a tool to create , communicate and simply cool!, and we called it <b>PlexyDesk</b>. 
					So the legacy begins, we officially announced the project on 2008 01 01. and the first pre alpha was demonstrated to <i>Dr, Richard Stallman</i> on his visit to Sri Lanka. 
					We consider this as a blessing and a fantastic start which would give life to our goals.
				</p>
				<p class="higlight">Goal and the Theme : PlexyDesk : A desktop to create , communicate and simply Cool</p>
				<ul class="list">
					<li>Create : PlexyDesk API allows users to create, use and publish small tools for their daily desktop needs.</li>
					<li>Communicate : Unified Contact management and Communication Mechanism allows easy management of tasks, people and files using the same API.</li>
					<li>Simply Cool : Tween based animations , and special effects makes a application that is a joy to use.</li>
				</ul>
				<h2>Features</h2>
				<p class="quote green">
					<b>Model View Design Pattern : </b> PlexyDesk API has a proper separation between the source of data and the view.The data providers are called engines, each engine will provide periodic data to it view when it’s available. This lets programmers good in low level programming skills to contribute with Engines and high level programmers and artists to contribute with usable and attractive user interfaces.
				</p>
				<p class="quote blue">
					<b>Live Icons : </b> Icons that know their content are called Live Icons. exmple a video icon would know how to play the video
					and a sound icon would know how to play sound.
				</p>
				<p class="quote green">
					<b>Intelligent Contact Management: </b> Social networking is not just a trend it’s life to many we know. PlexyDesk would be a on stop service station to manage and navigate social networks.
				</p>
				<p class="quote blue">
					<b>Automated Web Services to the Desktop: </b> Apple Desktop introduced the concept of Widgets to bring web services to the desktop
					plexydesk takes this a step beyond. Automatic via Automated service handling. example: drag and dropping a web RSS feed from
					picasa would create a Photo Album Widget.
				</p>
				<p class="quote green">
					<b>Unparalleled communication Power on Linux: </b> After listing to ideas of PlexyDesk few of the lead developers of tapioca voip joined our us, and as a team we are aiming to bring a communication platform and a Tool chain we have
					yet to see on Linux.
				</p>
				<p class="quote blue">
					<b>Integration and interoperability: </b> When we start we had two options make a new desktop environment or try to live with the desktop environments we have like KDE or GNOME, we choose the later as most of the developers are already part of those projects. PlexyDesk has been designed to work with Any version of KDE or GNOME and even Mac Os X but without Tapioca Support.
				</p>
